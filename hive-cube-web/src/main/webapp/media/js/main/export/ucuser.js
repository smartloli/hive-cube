function userCheckAll() {
	$("#checkall").click(function() {
		if (this.checked) {
			$("input[name='items']").each(function() {
				this.checked = true;
			});
		} else {
			$("input[name='items']").each(function() {
				this.checked = false;
			});
		}
	});
}
function initUcuser() {
	loadUcuser();
}

function loadUcuser() {
	$(ucuser).each(function() {
		var html = "";
		if (this.value == "_uid" || this.value == "_plat") {
			html = '<label class="checkbox-inline" title="' + this.valueName + '(' + this.value + ')">' + '<input type="checkbox" checked="checked" disabled  valuename="' + this.valueName + '" value="' + this.value + '" name="items">' + this.show + "(" + this.value + ")" + '</label>';
		} else {
			html = '<label class="checkbox-inline" title="' + this.valueName + '(' + this.value + ')">' + '<input type="checkbox" valuename="' + this.valueName + '" value="' + this.value + '" name="items">' + this.show + "(" + this.value + ")" + '</label>';
		}
		$("#hbase_user_info").append(html);
	});
	userCheckAll();
	$('[data-toggle="tooltip"]').tooltip();
}
